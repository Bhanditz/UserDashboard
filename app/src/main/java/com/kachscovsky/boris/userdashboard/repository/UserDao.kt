package com.kachscovsky.boris.userdashboard.repository

import android.arch.lifecycle.LiveData
import android.arch.persistence.room.Dao
import android.arch.persistence.room.Insert
import android.arch.persistence.room.OnConflictStrategy.REPLACE
import android.arch.persistence.room.Query

/**
 * Data Access Object interface which the Room library uses to create a class to interact with the
 * database given the annotated queries
 */
@Dao
interface UserDao {

    @Insert(onConflict = REPLACE)
    fun save(users: List<User>)

    @Query("SELECT * FROM user")
    fun load(): LiveData<List<User>>

    @Query("DELETE FROM user")
    fun deleteAll()

}